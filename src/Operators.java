public class Operators {
    public static void main(String[] args) {

        // + number (byte, short, int, long)
        byte a = 29;
        byte b = 5;

        System.out.println(a + b);

        // + String
        String firstName = "John";
        String lastName = "Doe";

        System.out.println(firstName + " " + lastName);
    }
}
